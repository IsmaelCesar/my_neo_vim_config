-- Cursor option 
vim.opt.number = true
vim.opt.relativenumber = true
vim.opt.cursorline = true
vim.opt.wrap = true -- Do not wrap line
vim.opt.scrolloff = 10 -- Setting 10 lines above and below the cursor
vim.opt.sidescrolloff = 8 -- Set 8 collumns to the left or to the right of the cursor

-- Tab options
vim.opt.tabstop = 2
vim.opt.shiftwidth = 2
vim.opt.softtabstop = 2
vim.opt.expandtab = true -- use spaces instead of tabs
vim.opt.smartindent = true -- smart auto ident from current file
vim.opt.autoindent = true -- copy auto ident from current line


-- Search settings
vim.opt.incsearch = true -- show matches as I type
